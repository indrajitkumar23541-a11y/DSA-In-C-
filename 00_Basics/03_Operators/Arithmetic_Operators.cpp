#include <iostream>
using namespace std;
int main(){
    int a = 23, b = 18;
    cout<<"a+b : "<<a+b<<endl; // Addition operator
    cout<<"a-b : "<<a-b<<endl; // Subtraction operator
    cout<<"a*b : "<<a*b<<endl; // Multiplication operator
    cout<<"a/b : "<<a/b<<endl; // Division operator
    cout<<"a%b : "<<a%b<<endl; // Modulo operator
    cout<<"a++ : "<<a++<<endl; // Post-increment opereator
    cout<<"a-- : "<<a--<<endl; // Post decrement operator
    cout<<"++b : "<<++b<<endl; // Pre-increment operator
    cout<<"--b : "<<--b<<endl; // Pre-decrement operator
    return 0;
    

}