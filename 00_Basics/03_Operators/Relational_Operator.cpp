#include <iostream>
using namespace std;
int main(){
    int a = 56, b = 48;
    cout<<"a > b : "<<(a>b)<<endl; // Grater than operator
    cout<<"a < b : "<<(a<b)<<endl; // Less than operator
    cout<<"a >= b : "<<(a>=b)<<endl; // Grater than or equal to operator
    cout<<"a <= b : "<<(a<=b)<<endl; // Less than or equal to operator
    cout<<"a == b : "<<(a==b)<<endl; // Equal to operator
    cout<<"a != b : "<<(a!=b)<<endl; // Not equal to operator
    return 0;
}